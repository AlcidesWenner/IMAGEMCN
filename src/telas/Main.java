    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        JFileChooser arquivo = new JFileChooser();
        FileNameExtensionFilter filtrosql = new FileNameExtensionFilter("Restaurar base", "sql");
        arquivo.addChoosableFileFilter(filtrosql);
        arquivo.setAcceptAllFileFilterUsed(false);
        if (arquivo.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            local_restaurar = arquivo.getSelectedFile().getAbsolutePath();
            try {
                Dump.restore(local_restaurar, "localhost", "3306", "root", "1914", "imagemcn");
                
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

